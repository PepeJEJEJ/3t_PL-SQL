DECLARE
    CURSOR C_MARCAS IS 
        SELECT ID_MARCA, MARCA FROM MARCAS_COCHE ORDER BY ID_MARCA;

    V_ID   MARCAS_COCHE.ID_MARCA%TYPE;
    V_NOM  MARCAS_COCHE.MARCA%TYPE;
    V_TOTAL NUMBER;
BEGIN
    OPEN C_MARCAS;
    LOOP
        FETCH C_MARCAS INTO V_ID, V_NOM;
        EXIT WHEN C_MARCAS%NOTFOUND;

        SELECT COUNT(*)
        INTO V_TOTAL
        FROM COCHE CO
        JOIN MODELO_COCHE MO ON CO.ID_MODELO = MO.ID_MODELO
        WHERE MO.ID_MARCA = V_ID;

        DBMS_OUTPUT.PUT_LINE(V_NOM || ': ' || V_TOTAL || ' COCHES');
    END LOOP;

    CLOSE C_MARCAS;
END;
/
