DECLARE
    V_DNI   VENDE.DNI_CLIENTE%TYPE := '&DNI';
    
    CURSOR C_VENTAS IS
        SELECT MATRICULA, FECHA, PRECIO
        FROM VENDE
        WHERE DNI_CLIENTE = V_DNI;
        
    V_MATRICULA VENDE.MATRICULA%TYPE;
    V_FECHA     VENDE.FECHA%TYPE;
    V_PRECIO    VENDE.PRECIO%TYPE;
BEGIN
    OPEN C_VENTAS;
    LOOP
        FETCH C_VENTAS INTO V_MATRICULA, V_FECHA, V_PRECIO;
        EXIT WHEN C_VENTAS%NOTFOUND;
        
        DBMS_OUTPUT.PUT_LINE('MATRICULA: '||V_MATRICULA||' | FECHA: '||V_FECHA||' | PRECIO: '||V_PRECIO);
    END LOOP;
    CLOSE C_VENTAS;
END;
