BEGIN
    FOR R IN (
        SELECT C.NOMBRE CLIENTE,E.NOMBRE EMPLEADO, M.MARCA, MO.DESCRIPCION MODELO, V.PRECIO
        FROM VENDE V
        JOIN CLIENTE C ON V.DNI_CLIENTE = C.DNI
        JOIN EMPLEADO E ON V.DNI_EMPLEADO = E.DNI
        JOIN COCHE CO ON V.MATRICULA = CO.MATRICULA
        JOIN MODELO_COCHE MO ON CO.ID_MODELO = MO.ID_MODELO
        JOIN MARCAS_COCHE M ON MO.ID_MARCA = M.ID_MARCA
    ) LOOP
        DBMS_OUTPUT.PUT_LINE(R.CLIENTE || '-' || R.EMPLEADO || '-' || R.MARCA || '-' || R.MODELO || '-' || R.PRECIO);
    END LOOP;
END;
