/*Pedir matrícula y calcular:  
beneficio = precio_venta – precio_compra
Mostrar ambos valores.*/
DECLARE
    V_MATRICULA COCHE.MATRICULA%TYPE := '&MATRICULA';
    V_PRECIO_COMPRA COCHE.PRECIO_COMPRA%TYPE;
    V_PRECIO_VENTA VENDE.PRECIO%TYPE;
    BENEFICIO NUMBER;
BEGIN
    SELECT PRECIO_COMPRA INTO V_PRECIO_COMPRA FROM COCHE
    WHERE MATRICULA = V_MATRICULA;

    SELECT PRECIO INTO V_PRECIO_VENTA FROM VENDE
    WHERE MATRICULA = V_MATRICULA;
    
    BENEFICIO := V_PRECIO_VENTA - V_PRECIO_COMPRA;

    DBMS_OUTPUT.PUT_LINE('PRECIO COMPRA: ' || V_PRECIO_COMPRA);
    DBMS_OUTPUT.PUT_LINE('PRECIO VENTA : ' || V_PRECIO_VENTA);
    DBMS_OUTPUT.PUT_LINE('BENEFICIO    : ' || BENEFICIO);
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('NO EXISTE ESA MATRÍCULA O NO TIENE VENTA REGISTRADA');
END;
