DECLARE
    V_MATRICULA COCHE.MATRICULA%TYPE := '&MATRICULA';
    V_PRECIO_COMPRA COCHE.PRECIO_COMPRA%TYPE;
    V_PRECIO_NUEVO NUMBER;
BEGIN
    SELECT PRECIO_COMPRA
    INTO V_PRECIO_COMPRA
    FROM COCHE
    WHERE MATRICULA = V_MATRICULA;

    V_PRECIO_NUEVO := V_PRECIO_COMPRA * 1.10;

    UPDATE COCHE
    SET PRECIO_COMPRA = V_PRECIO_NUEVO
    WHERE MATRICULA = V_MATRICULA;

    DBMS_OUTPUT.PUT_LINE('ANTES: ' || V_PRECIO_COMPRA);
    DBMS_OUTPUT.PUT_LINE('DESPUÉS: ' || V_PRECIO_NUEVO);
END;
/

